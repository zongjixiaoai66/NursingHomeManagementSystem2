		// 下载
		download(file){
			window.open(`${file}`)
		},
		// 初始化
		init(id,type) {
			if (id) {
				this.id = id;
				this.type = type;
			}
			if(this.type=='info'||this.type=='else'){
				this.info(id);
			}else if(this.type=='logistics'){
				this.logistics=false;
				this.info(id);
			}else if(this.type=='cross'){
				var obj = this.$storage.getObj('crossObj');
				for (var o in obj){
						if(o=='yihugonghao'){
							this.ruleForm.yihugonghao = obj[o];
							this.ro.yihugonghao = true;
							continue;
						}
						if(o=='yihuxingming'){
							this.ruleForm.yihuxingming = obj[o];
							this.ro.yihuxingming = true;
							continue;
						}
						if(o=='qingjialeixing'){
							this.ruleForm.qingjialeixing = obj[o];
							this.ro.qingjialeixing = true;
							continue;
						}

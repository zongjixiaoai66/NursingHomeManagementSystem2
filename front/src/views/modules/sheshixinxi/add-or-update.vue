    onSubmit() {






	if(this.ruleForm.sheshitupian!=null) {
		this.ruleForm.sheshitupian = this.ruleForm.sheshitupian.replace(new RegExp(this.$base.url,"g"),"");
	}







var objcross = this.$storage.getObj('crossObj');

      //更新跨表属性
